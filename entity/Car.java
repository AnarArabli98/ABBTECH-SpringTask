package com.anararabli.abbtech.entity;


public class Car {
    private long id;
    private String model;
    private String year;
    private String color;

    public Car() {
        this.id = id;
        this.model = model;
        this.year = year;
        this.color = color;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }





}
